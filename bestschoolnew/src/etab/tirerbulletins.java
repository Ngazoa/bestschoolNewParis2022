    private void periodeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_periodeItemStateChanged
        try {
            Curseur.jtablestartWaitCursor(jInternalFrame2);
            if (!"annuel".equals(periode.getSelectedItem().toString())) {
                new fonctionsBulletins().getNotesElevesPourBulletins(jTable1,
                        classe.getSelectedItem().toString(), periode.getSelectedItem().toString());
            } else {
                new fonctionsBulletins().getNotesElevesPourBulletinsAnnuel(jTable1,
                        classe.getSelectedItem().toString(), "sequence 1");

            }
            // TODO add your handling code here:
        } catch (SQLException ex) {
            Logger.getLogger(tirerbulletins.class.getName()).log(Level.SEVERE, null, ex);
        }
        Curseur.jablestopWaitCursor(jInternalFrame2);
    }//GEN-LAST:event_periodeItemStateChanged

    private void classeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_classeItemStateChanged
        Curseur.jtablestartWaitCursor(jInternalFrame2);
        Curseur.startJDIALOGWaitCursor(this);
        if (!"select".equals(periode.getSelectedItem().toString())) {
            if (!"annuel".equals(periode.getSelectedItem().toString())) {
                try {
                    new fonctionsBulletins().getNotesElevesPourBulletins(jTable1,
                            classe.getSelectedItem().toString(), periode.getSelectedItem().toString());
                } catch (SQLException ex) {
                    Logger.getLogger(tirerbulletins.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                try {
                    new fonctionsBulletins().getNotesElevesPourBulletinsAnnuel(jTable1,
                            classe.getSelectedItem().toString(), "sequence 1");
                } catch (SQLException ex) {
                    Logger.getLogger(tirerbulletins.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
            Curseur.jtablestopWaitCursor(jInternalFrame2);

        }
        Curseur.stoJDialogC(this);
        // TODO add your handling code here:
    }//GEN-LAST:event_classeItemStateChanged

    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        Curseur.jtablestartWaitCursor(jInternalFrame2);
        //Curseur.startWaitCursor(this);
        try {
            jTable1.getCellEditor().stopCellEditing();
        } catch (Exception ex) {

        }
        if (!periode.getSelectedItem().equals("select") && !classe.getSelectedItem().equals("select")) {

            if (periode.getSelectedItem().toString().equals("annuel")) {
                try {
                    new fonctionsBulletins().imprimerBulletinsAnnuel(jTable1,
                            periode.getSelectedItem()
                                    .toString(), classe.getSelectedItem().toString());
                } catch (Exception ex) {
                    Logger.getLogger(tirerbulletins.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {

                try {
                    new fonctionsBulletins().imprimerBulletinsTrimestriels(jTable1, periode.getSelectedItem()
                            .toString(), classe.getSelectedItem().toString());
                    // TODO add your handling code here:
                } catch (Exception ex) {
                    Logger.getLogger(tirerbulletins.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            new message().warning("Donnees manquantes", "Veuillez selectionner\nla periode et classe");
        }
Curseur.jablestopWaitCursor(jInternalFrame2);
    }//GEN-LAST:event_button1ActionPerformed

